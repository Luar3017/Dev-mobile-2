package com.example.gamefication.Objetos;

public class Habito {
   private String tituloH, observacaoH;
   private int  dificuldadeH;

    public String getTituloH() {
        return tituloH;
    }

    public void setTituloH(String tituloH) {
        this.tituloH = tituloH;
    }

    public String getObservacaoH() {
        return observacaoH;
    }

    public void setObservacaoH(String observacaoH) {
        this.observacaoH = observacaoH;
    }

    public int getDificuldadeH() {
        return dificuldadeH;
    }

    public void setDificuldadeH(int dificuldadeH) {
        this.dificuldadeH = dificuldadeH;
    }
}
