package com.example.gamefication.Objetos;

public class Tarefas {
    String tituloT, observacaoT;
    int dificuldadeT;

    public String getTituloT() {
        return tituloT;
    }

    public void setTituloT(String tituloT) {
        this.tituloT = tituloT;
    }

    public String getObservacaoT() {
        return observacaoT;
    }

    public void setObservacaoT(String observacaoT) {
        this.observacaoT = observacaoT;
    }

    public int getDificuldadeT() {
        return dificuldadeT;
    }

    public void setDificuldadeT(int dificuldadeT) {
        this.dificuldadeT = dificuldadeT;
    }
}
